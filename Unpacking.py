coordinates = (1, 2, 3)

# suppose x = 1, y = 2, z =3 and we want to multiply them.
# could do this:
# x = coordinates[0]
# y = coordinates[1]
# z = coordinates[2]

# OR unpack them like this:
#
x, y, z = coordinates

print(x)
print(y)
print(z)

# can do the same with Lists
coordinates = [1, 2, 3]
x, y, z = coordinates
print(x)
print(y)
print(z)